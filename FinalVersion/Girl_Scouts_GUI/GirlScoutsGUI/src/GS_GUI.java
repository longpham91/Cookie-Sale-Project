    private void orderSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderSubmitActionPerformed
        // TODO add your handling code here:
        
        try{
            String cust_name = firstNameOrderText.getText() + " " + lastNameOrderText.getText();
            String cust_address = streetOrderText.getText() + ", " + cityOrderText.getText() + ", " + "IN " + zipOrderText.getText();
            int phone = Integer.parseInt(phoneOrderText.getText());
            String email = emailOrderText.getText();
            
            SQLiteJDBC jdbc = new SQLiteJDBC();
            jdbc.insertCustomer(cust_name, cust_address, phone, email);
            
            boolean paid = paidCheck.isSelected();
            boolean pickedup = pickedCheck.isSelected();
            String scout_name = scoutFirstNameText.getText() + " " + scoutLastNameText.getText();
            int amount = 0;
            
            if (samoaCheck.isSelected()) {
                amount = Integer.parseInt(samoaText.getText());
                jdbc.insertOrder(cust_name, scout_name, "Samoas", amount, paid, pickedup);
            }
            
            if (savannahCheck.isSelected()) {
                amount = Integer.parseInt(savannahText.getText());
                jdbc.insertOrder(cust_name, scout_name, "Savannah Smiles", amount, paid, pickedup);
            }
            
            if (trefoilCheck.isSelected()) {
                amount = Integer.parseInt(trefoilText.getText());
                jdbc.insertOrder(cust_name, scout_name, "Trefoils", amount, paid, pickedup);
            }
            
            if (tagalongCheck.isSelected()) {
                amount = Integer.parseInt(tagalongText.getText());
                jdbc.insertOrder(cust_name, scout_name, "Tagalongs", amount, paid, pickedup);
            }
            
            if (thinMintCheck.isSelected()) {
                amount = Integer.parseInt(thinMintText.getText());
                jdbc.insertOrder(cust_name, scout_name, "Thin Mints", amount, paid, pickedup);
            }
            
            if (dosidoCheck.isSelected()) {
                amount = Integer.parseInt(dosidoText.getText());
                jdbc.insertOrder(cust_name, scout_name, "Do-Si-Dos", amount, paid, pickedup);
            }
            
            scoutFirstNameText.setText("Scout First Name");
            scoutLastNameText.setText("Scout Last Name");
            firstNameOrderText.setText("First Name");
            lastNameOrderText.setText("Last Name");
            phoneOrderText.setText("Phone Number");
            emailOrderText.setText("Email Address");
            streetOrderText.setText("Street Address");
            street2OrderText.setText("Street Address 2");
            cityOrderText.setText("City");
            zipOrderText.setText("Zip");
            savannahText.setText("0");
            trefoilText.setText("0");
            dosidoText.setText("0");
            samoaText.setText("0");
            tagalongText.setText("0");
            thinMintText.setText("0");
            paidCheck.setSelected(false);
            pickedCheck.setSelected(false);
            
            
            
            jFrame2.dispose();
            setEnabled(true);
            requestFocusInWindow();
            toFront();
            
        }
        catch (Exception e){
            JPanel error = new JPanel();
            JOptionPane.showMessageDialog(error, e.getMessage(), "Input Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_orderSubmitActionPerformed

