    private void totals(String s, int whichBox){
        int tempBoxes = 0;
        try{
            tempBoxes = Integer.parseInt(s);
            
        }
        catch(Exception e){
            System.out.println("Not a string");
        }
        totalBoxes += (tempBoxes-previousBox[whichBox]);
        total = totalBoxes * boxPrice;
        totalBoxLabel.setText(totalBoxString+totalBoxes);
        String tempTotal = String.format("%,6.2f",total);
        totalLabel.setText(totalString+tempTotal);
    }
