    private void searchTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchTextActionPerformed
        // TODO add your handling code here:
        //listModel.addElement("HI");
        try {
            String searchName = searchNameText.getText();
            SQLiteJDBC jdbc = new SQLiteJDBC();
            Vector<String> searchResult = jdbc.findScout(searchName);
            
            for (int i = 0; i < searchResult.size(); i++) {
                
                listModel.addElement(searchResult.get(i));
            }
                
        }
        catch (Exception e) {
            JPanel error = new JPanel();
            JOptionPane.showMessageDialog(error, e.getMessage(), "Input Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_searchTextActionPerformed

